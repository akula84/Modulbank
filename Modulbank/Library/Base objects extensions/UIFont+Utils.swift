import UIKit

extension UIFont {
    
    static var system14: UIFont {
        return .systemFont(ofSize: 14)
    }
    
    static var system15: UIFont {
        return .systemFont(ofSize: 15)
    }
    
    static var system16: UIFont {
        return .systemFont(ofSize: 16)
    }
    
}
